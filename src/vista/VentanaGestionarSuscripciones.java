    public void horaIncluidasTipo() {
        int indiceSeleccionado = cbTipoSuscri.getSelectedIndex();

        if (indiceSeleccionado == 1) {
            txtHorasPlan.setText("20");
        } else if (indiceSeleccionado == 2) {

            txtHorasPlan.setText("48");
        } else {

            txtHorasPlan.setText("");
        }

    }
