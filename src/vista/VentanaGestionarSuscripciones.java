    private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarClienteActionPerformed
        if (!txtCedula.getText().isEmpty()) {
            String cedula = txtCedula.getText();
            Usuario cliente = controller.buscarCliente(cedula);
            if (cliente != null) {
                txtNombreCliente.setText(cliente.getNombre());
                Usuario usuarioC = cliente;
                if (usuarioC instanceof Cliente) {
                    Cliente clien = (Cliente) usuarioC;
                    Suscripcion activa = controller.obtenerSuscripcionActiva(clien);
                    if (activa != null) {
                        panelSuscripcionActiva.setVisible(true);
                        //txtHorasUsadas.setText(clien.get);
                    } else {
                        btnCrearSuscripcion.setVisible(true);

                    }
                    //si ese cliente que se busco tiene suscripciones activo mostar un JOption y un boton que diga como "Este cliente ya tiene una suscripci√≥n activa" y deshabilitar el boton de nueva suscripcion 

                }
            } else {
                JOptionPane.showMessageDialog(null, "cliente no registrado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Asegurese de Digitar la c√©dula del cliente");
        }


    }//GEN-LAST:event_btnBuscarClienteActionPerformed

